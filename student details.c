/* C program to print details of a person */
#include<stdio.h>
void main()
{
	//variable declaration
	int age;
	float cgpa;
	char initial;
	
	//intialization
	age=19;
	cgpa=9.2;
	initial='P';
	
	//output
	printf("students age is:%i\n",age);
	printf("students cgpa is:%f\n",cgpa);
	printf("students initial is:%c",initial);
}
